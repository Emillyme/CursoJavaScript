//DECLARAÇÕES-----------------
// var
// Declara uma variável, opcionalmente, inicializando-a com um valor.

// let
// Declara uma variável local de escopo do bloco, opcionalmente, inicializando-a com um valor.
// sempre usar mais o const do que o let, usar apenas o let para se caso precisar alterar valores.

// const
// Declara uma constante. Constante são constantes, ou seja, não podem serem alteradas.


//alterando o valor com let
let valorIngressoAdulto = 20;
valorIngressoAdulto = 30; 
console.log(valorIngressoAdulto);

//alterar valor com const é impossível, pois ele é uma CONSTANTE 
const valorIngressoAdulto = 20;
//valorIngressoAdulto = 30;
console.log(valorIngressoAdulto);